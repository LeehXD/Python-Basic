n1 = int(input('Digite um numero:'))  #int informa para a maquina que o conteudo entre aspas será um numero inteiro e não uma "frase"
n2 = int(input('Digite um numero:'))
soma = n1+n2
print('A soma de {} e {} é igual a {}.'.format(n1, n2, soma)) #forma mais simples de escrever código print com muitas variaveis, simplificação
                                                                                                  # utilizando {} e .format() para informar a maquina as variaveis que serão inseridas em cada chave
